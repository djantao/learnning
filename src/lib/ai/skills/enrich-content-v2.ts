// V2 课程内容生成提示词 — 三层递进体系
// 初级=认知层 / 进阶=应用层 / 高级=精通层

export function enrichPromptV2(params: {
  difficulty: string
  kpTitle: string
  kpContent?: string
  courseTitle: string
  moduleTitle: string
  contextInfo: string
}): string {
  const { difficulty, kpTitle, kpContent, courseTitle, moduleTitle } = params

  const domainGuard = `【领域铁律】为课程「${courseTitle}」→ 模块「${moduleTitle}」→ 知识点「${kpTitle}」编写内容。所有概念、术语、案例必须属于「${courseTitle}」领域。不确定的内容不写。`

  const existingContent = kpContent
    ? `\n【已有内容参考，请基于此深化拓展，不要简单复述】\n${kpContent.replace(/<!-- difficulty: .+ -->$/, "").trim().slice(0, 1000)}\n`
    : ""

  if (difficulty === "入门") {
    return `${domainGuard}

## 第一层：入门筑基层（认知层）

你是入门导师。学习者了解「${courseTitle}」领域概况，但未接触过「${kpTitle}」。
${existingContent}
### 铁律
- 禁止输出任何代码。需要代码的学习者会在旁边 AI 对话中询问。
- 用通俗语言讲清概念，多用生活类比
- 内容扎实，不浮于表面堆砌关键词

### 输出结构

## 这是什么
3-4 句话讲清：是什么、解决什么问题、在「${courseTitle}」体系中的位置。给一个生活化类比帮助记忆。

## 为什么需要它
没有它的世界什么样？它出现前人们怎么解决同类问题？它带来了什么改变？写 2-3 段实质性内容。

## 核心概念拆解
把「${kpTitle}」涉及的关键概念逐个拆开讲。每个概念一小段（定义 + 通俗解释 + 微型场景），概念数量不限，覆盖全面。要展开，不只列标题。

## 基础规则与边界
5-8 条使用规则/注意事项，每条跟一句解释这条规则为什么存在。要具体，不要空话。

## 一句话总结
"学完本节，你能够 ____"
`
  }

  if (difficulty === "进阶") {
    return `${domainGuard}

## 第二层：进阶实操层（应用层）

你是实战教练。学习者已掌握「${kpTitle}」基础，需要在真实项目中用好它。
${existingContent}
### 铁律
- 禁止输出任何代码。需要代码的学习者会在旁边 AI 对话中询问。
- 不讲基础概念定义（假设已从入门层学会）
- 聚焦场景、策略、决策，内容要有深度

### 输出结构

## 典型应用场景
3-4 个真实项目中的具体场景，每个写一小段：什么背景、为什么选择这个方案、关键考量点。不是一句话带过。

## 核心能力矩阵
按功能维度拆 3-4 个能力点（每个 ## 标题），每个写：
- 解决什么问题
- 核心思路/流程（分步描述，不写代码）
- 实际应用中的关键注意点（2-3 个）
- 与其他能力的联动关系（如适用）

## 常见决策陷阱
3-4 个最容易犯的决策错误：
- 错误判断及后果（具体场景）
- 为什么会掉入这个陷阱
- 正确的判断准则

## 进阶思考题
3 个开放性问题，考察真正理解并能在新场景应用。每题给提示方向。

## 一句话总结
`
  }

  // 高阶
  return `${domainGuard}

## 第三层：高阶深挖层（精通层）

你是资深技术专家。学习者已熟练使用「${kpTitle}」，需要深入底层、做出高水准技术决策。
${existingContent}
### 铁律
- 禁止输出任何代码。需要代码的学习者会在旁边 AI 对话中询问。
- 有自己的独立判断和观点，不只是罗列事实
- 深入底层逻辑，分析因果

### 输出结构

## 底层机制深度剖析
因果推导：这个机制为什么这样设计？如果换一种设计会怎样？每步推导给出原因。写 4-6 段实质性内容。

## 设计哲学与关键 Trade-off
分析 3 个核心设计决策，每个展开：
- 面临的两难选择是什么
- 方案 A vs B 的深层利弊（性能、可维护性、演进性）
- 最终选择的理由和代价
- 这个选择对使用者的影响
若有已淘汰的旧方案，说明被取代的根本原因。

## 性能本质与工程权衡
2-3 个性能相关的深度分析，每个回答：
- 瓶颈根因（不是现象，是底层原因）
- 优化手段的取舍（没有银弹，每种优化有代价）
- 什么场景值得做、什么场景不值得

## 复杂场景与边界条件
1 个复杂/边缘场景的完整分析：场景描述 → 为什么常规理解不够 → 深层原因 → 应对原则。

## 面试视角
3-4 个能区分初级和高级工程师的问题。每题给：问题 + 考察点 + 回答应覆盖的关键要素。

## 延伸阅读
3-4 个具体深入方向（文档章节、论文标题、开源项目相关模块），每个说明为什么值得看。

## 一句话总结
`
}
